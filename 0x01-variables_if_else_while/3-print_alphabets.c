#include <stdio.h>

/**
 * main - entry point
 * description: print alphabet in lower & upper case
 * return 0 9success)
*/

int main(void)
{
	char l;

	char L;

	l = 'a';
	L = 'A';
	while
		(l <= 'z')
		{
			putchar(l);
			l++;
		}
	while
		(L <= 'Z')
		{
			putchar(L);
			L++;
		}
	putchar('\n');
	return (0);
}
